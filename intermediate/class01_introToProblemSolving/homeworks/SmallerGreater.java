    public static int solve(int[] A) {

        if(A == null || A.length ==2){
            return 0;
        }

        Arrays.sort(A);
        int count = 0;

        for (int i = 1; i <A.length-1; i++) {
           if(  A[0] < A[i]  && A[i] < A[A.length-1] ) {
                count++;
            }
        }
        return count;
    }

